package EjerciciosCLASS;

public class forma {
	
	private int area ; 
	private String dibujar;  
	private String id ; 
	
	
 public forma (int area , String dibujar , String id ) {
	 this.area= area ; 
	 this.dibujar= dibujar ; 
	 this.id= id ; 
	 
 }




/**
 * @param area the area to set
 */
public void setArea(int area) {
	this.area = area;
}




/**
 * @param dibujar the dibujar to set
 */
public void setDibujar(String dibujar) {
	this.dibujar = dibujar;
}





/**
 * @param id the id to set
 */
public void setId(String id) {
	this.id = id;
}
 
	
}

